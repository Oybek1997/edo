    public function destroy($id)
    {
        $model = Comment::find($id);
        $model->delete();

        return "Deleted!!!";
    }

    public function getComments(Request $request)
    {
        // return Auth::id();
        $comments = Comment::where('created_by', Auth::id());

        if($request['search']){
            $comments->where('text', 'like', '%'.$request['search'].'%');
        }
        return $comments->paginate(20);
    }
